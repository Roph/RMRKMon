e_chance is encounter. c_chance is catch. duh.


affinity is which "version" the pokemon is unique to. Trainers (will) have a "version" assigned, similar to pokemon red/blue, gold/silver and so on.

Affinity of 0 means the pokemon is globally available. 
Affinity of 1 means the pokemon is our equivalent of red, gold, ruby etc. The first in paired versions of games.
Affinity of 2 is the opposite. Blue, silver, sapphire and so on.

*/

