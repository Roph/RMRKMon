function cprite($poke, $link = false) { //Outputs a CSS-based sprite of the pokemon given. Auto detects shiny or not, and defaults to 96x96 base image. Optionally makes the sprite clickable, as a link to the pokemon's page.

	global $pokemon;

	//First off, which sprite sheet does this poke refer to?
	$pid = round($poke);
	if ($pid >= 0 && $pid <= 251) $sheet = 'g12';
	elseif ($pid >= 252 && $pid <= 493) $sheet = 'g34';
	elseif ($pid >= 494 && $pid <= 719) $sheet = 'g56';
	else die('<!-- WARNING - Unknown Pokemon For CSS Sprite! -->');
	
	//Is this pokemon shiny?
	if (is_shiny($poke)) $suffix = 's';
	else $suffix = 'n';
	
	//Finally, zero pad our poke.
	$p = sprintf("%03d",$poke);
	
	if ($link) echo '
	<a class="cprite '.$sheet.$suffix.' p'.$p.'" href="?pokemon='.$pid.';'.$pokemon[$pid].'"></a>';
	else echo '
	<div class="cprite '.$sheet.$suffix.' p'.$p.'"></div>';
	
}

